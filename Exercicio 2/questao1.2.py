n1 = float(input("Informe o 1º Número: "))
n2 = float(input("Informe o 2º Número: "))

print(f"O 1º numero foi {n1}.\nO 2º numero foi {n2}")

soma = n1+n2
menos = n1-n2
multi = n1*n2
divi = n1/n2
pot = n1**n2

print(f"O resultado da soma deu = {soma}.\nO resultado da subtração foi = {menos}\nO resultado da multiplicação foi = {multi}\nO resultado da divisão foi = {divi}")
print(f"O 1º numero elevado ao 2º foi = {pot}")